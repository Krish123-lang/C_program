#include <stdio.h>
int main()
{
    // char ch = 'a';
    char ch = 'z'; // used for printing in reverse order z to a

    // ******************************************************************************************************************
    // Prints a to z

    for (char ch = 'a'; ch <= 'z'; ch++)
    {
        printf("%c\n", ch);
    }

    // Prints z to a
    /*
    for (char ch = 'z'; ch >= 'a'; ch--)
    {
        printf("%c\n", ch);
    }
    */
    // ******************************************************************************************************************

    // ******************************************************************************************************************

    // Prints a to z

    while (ch <= 'z')
    {
        printf("%c\n", ch);
        ch++;
    }

    // prints z to a
    /*
     while (ch >= 'a')
     {
         printf("%c\n", ch);
         ch--;
     }
     */
    // ******************************************************************************************************************

    // ******************************************************************************************************************

    // Prints a to z

    do
    {
        printf("%c\n", ch);
        ch++;
    } while (ch <= 'z');

    // prints z to a
    /*
     do
     {
         printf("%c\n", ch);
         ch--;
     } while (ch >= 'a');
     */
    // ******************************************************************************************************************
    return 0;
}